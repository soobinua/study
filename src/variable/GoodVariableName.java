package variable;

public class GoodVariableName { // 좋은 변수명

	public static void main(String[] args) {
		int age = 17;
		int ageAfter10Years = age + 10;
		System.out.println(ageAfter10Years);

		// 10년후의 나이를 출력해주는 프로그램

		// 자바 변수명 규칙
		// 카멜케이스 표기법 : 시작 소문자 그 뒤 단어 첫 글자만 대문자 사용
		// 자바 예약어(reserved words) 변수명으로 사용불가
	}

}
