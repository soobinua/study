package variable;

public class AgeOfGildong {

	public static void main(String[] args) {
		// 길동이 현재 나이 8세.
		// 현재 길동이의 나이와 10년 후, 20년 후, 30년 후 길동이의 나이 출력

		int ageOfGildong = 8;
		// 길동 나이 9세일 때 10년 후, 20년 후, 30년 후 길동이의 나이 출력
		ageOfGildong = 9; // 변수 값만 바꾸면 된다.
		System.out.println(ageOfGildong);
		System.out.println(ageOfGildong + 10);
		System.out.println(ageOfGildong + 20);
		System.out.println(ageOfGildong + 30);

	}

}
