package datatype;

public class String03 {

	public static void main(String[] args) {
		// 자료형 - 문자열
		String suffix = "살 안 쪄요.";
		// 문자열은 + 연산자를 통해 여러개의 문자열 이어 붙이는 것 가능
		String chicken = "치킨은 " + suffix;
		String me = "나도" + suffix;
		System.out.println(chicken);
		System.out.println(me);

	}

}
