package datatype.integer;

public class IntegerData01 {

	public static void main(String[] args) {
		// 정수형 데이터 타입
		// 데이터 타입별로 표현 범위와 데이터 크기가 다르다.
		byte byteInteger = 10; 		// 1 byte
		short shortInteger = 20; 	// 2 byte
		int intInteger = 30; 		// 4 byte
		long longInteger = 40; 		// 8 byte
		System.out.println(byteInteger);
		System.out.println(shortInteger);
		System.out.println(intInteger);
		System.out.println(longInteger);

	}

}
