package datatype;

public class RealNumberData01 {

	public static void main(String[] args) {
		// 실수형 데이터 타입
		// float형 사용할 때는 숫자 뒤에 F 혹은 f 붙여주어야 한다.
		float floatNumber = 0.1f; 	// 4 byte
		double doubleNumber = 0.2; 	// 8 byte
		System.out.println(floatNumber);
		System.out.println(doubleNumber);
	}

}
