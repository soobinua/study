package datatype;

public class VariableExample {

	public static void main(String[] args) {
		// 이름, 나이, 주소를 변수로 선언해 문자열들 출력

		String name = "홍길동석";
		int age = 50;
		String address = "서울시";

		System.out.println("이름 : " + name);
		System.out.println("나이 : " + age);
		System.out.println("사는곳 : " + address);

	}

}
